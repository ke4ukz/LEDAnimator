// Promote the current Rust firmware build to the website — a DELIBERATE, SEPARATE
// step from firmware development. Run it only when a firmware build is validated
// and you want the site to ship it:
//
//     npm run promote-firmware
//
// It copies firmware-rust/led-animator.uf2 into the web assets (a stable name) and
// regenerates src/export/firmwareRelease.generated.ts with the version + flash
// layout it read straight from the firmware source, so the site's advertised
// version always matches what the device reports. Review the diff + commit.
//
// Firmware development can churn freely; nothing the site ships changes until this
// runs. Pass a path to override the UF2:  npm run promote-firmware -- path/to.uf2

import { readFileSync, writeFileSync, copyFileSync, existsSync, statSync } from 'node:fs'
import { execFileSync } from 'node:child_process'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const webRoot = resolve(here, '..')
const fwRoot = resolve(webRoot, '..', 'firmware-rust')

const uf2Src = process.argv[2] ? resolve(process.argv[2]) : resolve(fwRoot, 'led-animator.uf2')
const uf2Dest = resolve(webRoot, 'src/assets/led-animator-rp2040.uf2')
const metaDest = resolve(webRoot, 'src/export/firmwareRelease.generated.ts')

function fail(msg) {
  console.error(`promote-firmware: ${msg}`)
  process.exit(1)
}

if (!existsSync(uf2Src)) {
  fail(`no firmware UF2 at ${uf2Src}\n  build it first (cargo build --release + elf2uf2-rs), or pass a path.`)
}

/** Pull a value out of a firmware source file by regex (single source of truth). */
function grab(file, re, label) {
  const m = readFileSync(resolve(fwRoot, file), 'utf8').match(re)
  if (!m) fail(`couldn't read ${label} from firmware-rust/${file}`)
  return m[1]
}

const fwVersion = grab('src/protocol.rs', /FW_VERSION:\s*&str\s*=\s*"([^"]+)"/, 'FW_VERSION')
const blockSize = Number(grab('src/fs.rs', /FS_BLOCK_SIZE:\s*usize\s*=\s*(\d+)/, 'FS_BLOCK_SIZE'))
const blockCount = Number(grab('src/fs.rs', /FS_BLOCK_COUNT:\s*usize\s*=\s*(\d+)/, 'FS_BLOCK_COUNT'))
const fsOffsetHex = grab('src/fs.rs', /FS_OFFSET:\s*u32\s*=\s*0x([0-9a-fA-F_]+)/, 'FS_OFFSET').replace(/_/g, '')
const fsBaseAddr = 0x10000000 + parseInt(fsOffsetHex, 16)

// Firmware source provenance: the firmware-rust commit + whether the tree is clean.
let commit = 'unknown'
let dirty = false
try {
  commit = execFileSync('git', ['-C', fwRoot, 'rev-parse', '--short', 'HEAD']).toString().trim()
  dirty = execFileSync('git', ['-C', fwRoot, 'status', '--porcelain', '--', '.']).toString().trim().length > 0
} catch {
  /* not a git checkout — leave defaults */
}

const bytes = statSync(uf2Src).size
const builtAt = new Date().toISOString().slice(0, 10)

copyFileSync(uf2Src, uf2Dest)

const meta = `// GENERATED by scripts/promote-firmware.mjs — do NOT edit by hand.
// The pinned LED Animator RP2040 firmware the website ships. Bump it with a
// deliberate \`npm run promote-firmware\` when a new build is ready — never
// automatically. The UF2 itself is src/assets/led-animator-rp2040.uf2.
export const FIRMWARE_RELEASE = {
  /** What the device reports over VERSION — the site advertises the same string. */
  version: ${JSON.stringify(fwVersion)},
  /** firmware-rust source commit this build was promoted from. */
  commit: ${JSON.stringify(commit)},
  /** Date the build was promoted to the site (UTC). */
  builtAt: ${JSON.stringify(builtAt)},
  /** UF2 size in bytes. */
  bytes: ${bytes},
  board: 'Raspberry Pi Pico W (RP2040)',
  // Flash / littlefs layout the export splices the FS image into. Read from
  // firmware-rust/src/fs.rs, so it can't drift from the firmware.
  blockSize: ${blockSize},
  blockCount: ${blockCount},
  fsBase: 0x${fsBaseAddr.toString(16)},
} as const
`
writeFileSync(metaDest, meta)

const kb = (n) => `${(n / 1024).toFixed(0)} KB`
console.log('promoted firmware to the website:')
console.log(`  version   ${fwVersion}   (commit ${commit}${dirty ? ', TREE DIRTY' : ''})`)
console.log(`  uf2       ${kb(bytes)}  ->  src/assets/led-animator-rp2040.uf2`)
console.log(`  fs        ${blockCount} × ${blockSize} B @ 0x${fsBaseAddr.toString(16)}`)
console.log(`  meta      src/export/firmwareRelease.generated.ts`)
if (dirty) {
  console.log('\n  WARNING: firmware-rust has uncommitted changes — this UF2 may not match commit ' + commit + '.')
}
console.log('\nReview the diff, then commit both files to ship it.')
